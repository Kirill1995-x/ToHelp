package com.tohelp.tohelp.lists;

public class TitlesMain
{
    private int img;
    private String description;

    public TitlesMain(int img, String description)
    {
        this.img=img;
        this.description=description;
    }

    int getImg()
    {
        return this.img;
    }

    String getDescription()
    {
        return this.description;
    }
}
